import 'dart:io';

import 'package:dart_style/dart_style.dart';
import 'package:html/parser.dart' show parse;
import 'package:http/http.dart' as http;

const wikiUrl = 'https://en.wikipedia.org/wiki/List_of_ISO_639-2_codes';
const outputFile = 'lib/src/language_codes.dart';

Future<void> main() async {
  print('Fetching Wikipedia page...');
  final response = await http.get(Uri.parse(wikiUrl));
  if (response.statusCode != 200) {
    stderr.writeln('Failed to fetch page: HTTP ${response.statusCode}');
    exit(1);
  }

  final document = parse(response.body);

  final table = document.querySelector('table.wikitable');
  if (table == null) {
    stderr.writeln('Failed to locate the codes table.');
    exit(1);
  }

  final rows = table.querySelectorAll('tr');
  final entries = <_LangEntry>[];

  for (final row in rows.skip(1)) {
    final cols = row.querySelectorAll('td');
    if (cols.length < 8) continue;

    final code3Raw = cols[0].text.trim(); // e.g. "alb/sqi"
    final code1 = cols[3].text.trim(); // ISO 639-1 (2 letters, optional)

    // English names: pick the first
    final englishNames = cols[4]
        .text
        .trim()
        .split(';')
        .map((e) => _cleanName(e.trim()))
        .where((e) => e.isNotEmpty)
        .toList();
    final name = englishNames.isNotEmpty ? englishNames.first : '';

    // Native names: prefer script form (if comma separated: script + translit)
    final nativeCandidates = cols[7]
        .text
        .trim()
        .split(';')
        .expand((part) => part.split(',')) // split also by comma
        .map((e) => _cleanName(e.trim()))
        .where((e) => e.isNotEmpty)
        .toList();

    String nativeName =
        nativeCandidates.isNotEmpty ? nativeCandidates.first : '';

    String commonLatinName = '';
    String commonNonLatinName = '';
    if (nativeCandidates.isNotEmpty) {
      final nonLatin = nativeCandidates.firstWhere(
        (e) => !_isPureLatin(e),
        orElse: () => '',
      );
      final latin = nativeCandidates.firstWhere(
        (e) => _isPureLatin(e),
        orElse: () => '',
      );

      if (nonLatin.isNotEmpty) {
        commonNonLatinName = nonLatin;
      }

      if (latin.isNotEmpty) {
        commonLatinName = latin;
      }
    }

    if (code3Raw.isEmpty || name.isEmpty) continue;

    // Add two-letter code if available
    if (code1.isNotEmpty) {
      entries.add(_LangEntry(
          code1, name, nativeName, commonLatinName, commonNonLatinName));
    }

    // Handle multiple 3-letter codes (bibliographic/terminologic)
    final code3List =
        code3Raw.split('/').map((c) => c.trim()).where((c) => c.isNotEmpty);
    for (final code3 in code3List) {
      entries.add(_LangEntry(code3.replaceAll('*', ''), name, nativeName,
          commonLatinName, commonNonLatinName));
    }
  }

  // Add missing codes from _OldLanguageCodes to entries
  for (final oldCode in _OldLanguageCodes.values) {
    if (!entries.any((e) => e.code == oldCode.code)) {
      final code = entries.firstWhere(
        (e) => oldCode.code.startsWith('${e.code}_'),
        orElse: () => _LangEntry('', '', '', '', ''),
      );

      entries.add(_LangEntry(
        oldCode.code,
        oldCode.name,
        oldCode.nativeName,
        code.nativeLatinName,
        code.nativeNonLatinName,
      ));
    }
  }

  // Sort: 2-letter codes first, then 3-letter
  entries.sort((a, b) {
    final isA2 = _isTwoLetter(a.code);
    final isB2 = _isTwoLetter(b.code);

    if (isA2 && !isB2) return -1;
    if (!isA2 && isB2) return 1;
    return a.code.compareTo(b.code);
  });

  final buffer = StringBuffer()
    ..write('''
// ignore_for_file: constant_identifier_names

import 'dart:ui';

/// LanguageCodes(code, name in English, name in native).
///   - [code]: language code.
///   - [englishName]: preferred English name.
///   - [nativeName]: preferred native name.
///   - [nativeLatinName]: preferred native latin name (if available).
///   - [nativeNonLatinName]: preferred native non-latin name (if available).
///
///  @Source
///   - https://en.wikipedia.org/wiki/List_of_ISO_639-2_codes
///   - https://stackoverflow.com/questions/3217492/list-of-language-codes-in-yaml-or-json
/// 
///  Generated by ChatGPT and the crawler by lamnhan066
''')
    ..write('enum LanguageCodes {');

  for (var i = 0; i < entries.length; i++) {
    final e = entries[i];
    var enumName = e.code.replaceAll('-', '_').replaceAll('\n', '');
    // avoid reserved words
    if (enumName == 'is') enumName = 'is_';
    if (enumName == 'new') enumName = 'new_';

    final comma = i == entries.length - 1 ? ';' : ',';

    buffer
      ..writeln(
          '  /// code: "${e.code}", englishName: "${e.name}", nativeName: "${e.nativeName}", nativeLatinName: r"${e.nativeLatinName}", nativeNonLatinName: r"${e.nativeNonLatinName}"')
      ..writeln(
          '  $enumName("${e.code}", r"${e.name}", r"${e.nativeName}", r"${e.nativeLatinName}", r"${e.nativeNonLatinName}")$comma')
      ..writeln();
  }

  buffer.writeln(r'''
  /// Language code.
  ///
  /// Use [locale] if you want to get the current language as `Locale` because
  /// this [code] may contains country code.
  final String code;

  /// Get the English name of this code.
  final String englishName;

  /// Get the preferred native name of this code.
  final String nativeName;

  /// Preferred latin native name.
  final String nativeLatinName;

  /// Preferred non-latin native name.
  final String nativeNonLatinName;

  /// Get current code as Locale.
  Locale get locale {
    final localeList = code.split('_');

    switch (localeList.length) {
      case 1:
        return Locale(localeList[0]);
      case 2:
        // localeList[1] is the country code if all characters are upper cases.
        if (localeList[1] == localeList[1].toUpperCase()) {
          return Locale(localeList[0], localeList[1]);
        }
        return Locale.fromSubtags(
          languageCode: localeList[0],
          scriptCode: localeList[1],
        );
      default:
        return Locale.fromSubtags(
          languageCode: localeList[0],
          scriptCode: localeList[1],
          countryCode: localeList[2],
        );
    }
  }

  /// Get the English name.
  String get name => englishName;

  /// Get [LanguageCodes] from string [code]. If no matching element is found,
  /// returns the result of [orElse]. If [orElse] is omitted, it defaults to
  /// throwing a [StateError].
  static LanguageCodes fromCode(
    String code, {
    LanguageCodes Function()? orElse,
  }) {
    return LanguageCodes.values.singleWhere(
      (element) => element.code == code,
      orElse: orElse,
    );
  }

  /// Get [LanguageCodes] from [englishName]. If no matching element is found,
  /// returns the result of [orElse]. If [orElse] is omitted, it defaults to
  /// throwing a [StateError].
  static Iterable<LanguageCodes> fromEnglishName(String englishName) {
    return LanguageCodes.values.where(
      (element) => element.englishName == englishName,
    );
  }

  /// Get [LanguageCodes] from [nativeName]. If no matching element is found,
  /// returns the result of [orElse]. If [orElse] is omitted, it defaults to
  /// throwing a [StateError].
  static Iterable<LanguageCodes> fromNativeName(String nativeName) {
    return LanguageCodes.values.where(
      (element) => element.nativeName == nativeName,
    );
  }

  /// Get [LanguageCodes] from [Locale]. If no matching element is found,
  /// returns the result of [orElse]. If [orElse] is omitted, it defaults to
  /// throwing a [StateError].
  static LanguageCodes fromLocale(
    Locale locale, {
    LanguageCodes Function()? orElse,
  }) {
    for (final value in LanguageCodes.values) {
      if (locale == value.locale) {
        return value;
      }
    }
    if (orElse != null) {
      return orElse();
    }
    return throw StateError("No element");
  }

  /// LanguageCodes(code, name in English, name in native).
  ///   - [code]: language code.
  ///   - [name]: name in English.
  ///   - [nativeName]: name in native.
  ///   - [nativeLatinName]: preferred native name in Latin.
  ///   - [nativeNonLatinName]: preferred native name in non-Latin.
  const LanguageCodes(this.code, this.englishName, this.nativeName, this.nativeLatinName, this.nativeNonLatinName);
  ''');

  buffer.writeln('}');

  final formatter =
      DartFormatter(languageVersion: DartFormatter.latestLanguageVersion);
  final formatted = formatter.format(buffer.toString());

  await File(outputFile).writeAsString(formatted);
  print('✅ Generated Dart file: $outputFile');
}

class _LangEntry {
  final String code;
  final String name;
  final String nativeName;
  final String nativeLatinName;
  final String nativeNonLatinName;
  _LangEntry(this.code, this.name, this.nativeName, this.nativeLatinName,
      this.nativeNonLatinName);
}

String _cleanName(String name) {
  var cleaned = name
      // Remove years like (1453–), (9th–11th c.)
      .replaceAll(RegExp(r'\([^)]*[\d–cC][^)]*\)'), '')
      // Remove qualifiers like ", Modern", ", Old", ", Middle"
      .replaceAll(RegExp(r',\s*(Modern|Old|Middle)', caseSensitive: false), '')
      // Remove trailing " languages"
      .replaceAll(RegExp(r'\s*languages$', caseSensitive: false), '')
      // Collapse multiple spaces
      .replaceAll(RegExp(r'\s+'), ' ')
      // Remove trailing commas/semicolons
      .replaceAll(RegExp(r'[;,]\s*$'), '')
      .trim();

  return cleaned;
}

bool _isTwoLetter(String code) =>
    code.length == 2 || (code.length > 2 && code[2] == '_');

/// Detect if a string is purely Latin-based (ASCII + Latin Extended letters)
bool _isPureLatin(String text) {
  final latinRegex = RegExp("^[\\p{Script=Latin}\\s'-]+\$", unicode: true);
  return latinRegex.hasMatch(text);
}

